class force:
    xforce = float(0);
    yforce = float(0);
    zforce = float(0);
    def setxforce(x):
        xforce = x;
    def setyforce(y):
        yforce = y;
    def setzforce(z):
        zforce = z;
    def getxforce():
        return xforce;
    def getyforce():
        return yforce;
    def getzforce():
        return zforce;
